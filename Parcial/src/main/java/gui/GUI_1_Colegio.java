    private void AsignarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AsignarMateriaActionPerformed
       String infoDoc = "";
        for(Empleado emp : listaEmpleados) {
            if(emp.getNombre().equals(seleccionadoDocente)){
                String tipoemp = emp.getClass()+"";
              if(tipoemp.equals("class docente.Docente")){
                  Docente doc = (Docente) emp;
              String materiaSelec = (String) MateriasDisponibles.getSelectedItem();
              doc.setAsignatura(materiaSelec);
              infoDoc = doc.GetInfo();
             
            }
            }
             
        }
        String textoant = HistorialAsignaciones.getText();
        HistorialAsignaciones.setText(textoant + infoDoc + "\n");
           
    }//GEN-LAST:event_AsignarMateriaActionPerformed

