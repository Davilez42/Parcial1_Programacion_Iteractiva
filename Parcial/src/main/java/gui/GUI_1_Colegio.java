    private void AsignarTecnologiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AsignarTecnologiaActionPerformed
        String infoPer = "";
        for (Empleado emp :listaEmpleados) {
            if(emp.getNombre().equals(SeleccionadoPersonalAdmin)){
                String tipoemp = emp.getClass()+"";
              if(tipoemp.equals("class personalAdministrativo.PersonalAdministrativo")){
                  PersonalAdministrativo perAdmin = (PersonalAdministrativo) emp;
              String tecnologiaSelec = (String) TecnologiaDisponible.getSelectedItem();
              perAdmin.setTecnologiasManejadas(tecnologiaSelec);
              infoPer = perAdmin.GetInfo();
             
            }
            }
        }
        
        String textoant = HistorialAsignaciones.getText();
        HistorialAsignaciones.setText(textoant + infoPer + "\n");
        
    }//GEN-LAST:event_AsignarTecnologiaActionPerformed

